#include <iostream>
using namespace std;

int main() {
	int a, b, c;

	cin >> a >> b >> c;

	if (a % 2 == 0) {
		cout << "even" << '\n';
	}

	if (a % 2 != 0) {
		cout << "odd" << '\n';
	}

	if (b % 2 == 0) {
		cout << "even" << '\n';
	}

	if (b % 2 != 0) {
		cout << "odd" << '\n';
	}

	if (c % 2 == 0) {
		cout << "even" << '\n';
	}

	if (c % 2 != 0) {
		cout << "odd" << '\n';
	}


	return 0;
}